function generateProduct(id: number, name: string, category: string, price: number, salePrice?: number, sku?: string): Product {
  const slug = name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '');
  return {
    id: `woo-${id}`,
    name,
    slug,
    description: `High-quality ${name.toLowerCase()} from our ${category.toLowerCase()} collection. Premium materials and excellent craftsmanship make this product a must-have.`,
    shortDescription: `Premium ${name.toLowerCase()} with exceptional quality and design.`,
    price: salePrice || price,
    regularPrice: price,
    salePrice: salePrice,
    onSale: !!salePrice,
    stockStatus: 'instock',
    stockQuantity: Math.floor(Math.random() * 100) + 10,
    categories: [{ id: categories.find(c => c.slug === category.toLowerCase().replace(/[^a-z0-9]+/g, '-'))?.id || 1, name: category, slug: category.toLowerCase().replace(/[^a-z0-9]+/g, '-'), count: 1 }],
    tags: [{ id: id, name: category, slug: category.toLowerCase().replace(/[^a-z0-9]+/g, '-') }],
    images: [{ id: id, src: `https://picsum.photos/seed/${id}/600/600`, alt: name, name: `${name} image` }],
    attributes: [],
    relatedIds: [],
    averageRating: Number((Math.random() * 2 + 3).toFixed(1)),
    reviewCount: Math.floor(Math.random() * 200) + 5,
    featured: Math.random() > 0.8,
    sku: sku || `SKU-${category.substring(0, 3).toUpperCase()}-${String(id).padStart(4, '0')}`,
    createdAt: new Date(Date.now() - Math.random() * 30 * 24 * 60 * 60 * 1000).toISOString(),
    updatedAt: new Date().toISOString(),
  };
}

